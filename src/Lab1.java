    static String decimalToBinaryString(int decimal)
    {
        System.out.println("Hello!");
        String result = "";
        int rest = decimal % 2;
        result = Integer.toString(rest) + result;
        while (decimal > 0)
        {
            decimal = decimal / 2;
            if (decimal != 0)
            {
                rest = decimal % 2;
                result = Integer.toString(rest) + result;
            }

        }

        return result;

    }

